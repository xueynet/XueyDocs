<template><h1 id="后台显示内容点击量"><a class="header-anchor" href="#后台显示内容点击量">#</a> 后台显示内容点击量</h1>
<p>后台默认不显示内容点击量，需要在内容列表中设置显示项。</p>
<h3 id="设置显示项"><a class="header-anchor" href="#设置显示项">#</a> 设置显示项</h3>
<p>进入左侧菜单 <code>站点管理 -&gt; 内容管理</code>，进入对应栏目，点击页面右侧的 <code>显示项</code> 按钮：</p>
<p><img src="/assets/img/plugin/hints/02.png" alt="02"></p>
<p>对应的显示项说明如下：</p>
<ul>
<li><code>点击量(Hits)</code>：内容总点击量</li>
<li><code>日点击(HitsByDay)</code>：内容今天的点击量</li>
<li><code>周点击(HitsByWeek)</code>：内容本周的总点击量</li>
<li><code>月点击(HitsByMonth)</code>：内容本月的总点击量</li>
<li><code>最后点击时间(LastHitsDate)</code>：内容最后一次被访问的时间</li>
</ul>
<p>其中，日点击、周点击以及月点击只有设置了 <code>统计总点击量以及本日、本周、本月点击量</code> 选项后才会统计数字。</p>
<h3 id="查看内容点击量"><a class="header-anchor" href="#查看内容点击量">#</a> 查看内容点击量</h3>
<p>勾选点击量显示项后，可以看到点击量相关属性已经显示在列表中了：</p>
<p><img src="/assets/img/plugin/hints/03.png" alt="03"></p>
<p>点击前台页面后刷新内容列表将可以看到数字变化。</p>
</template>